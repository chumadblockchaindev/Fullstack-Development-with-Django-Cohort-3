import math
# Maths Operators
# # +, -, *, /, %, **, //

a = 15
b = 4
print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a % b)  # Modulus
print(a ** b) # Exponentiation
print(a // b) # Floor Division
print(math.sqrt(16)) # Square root
print(math.pow(a, b)) # Power function
print(math.pi) # Pi constant

